use cadastro;
# Crindo backup do Banco de Dados:
/* Em Workbench
Clica em Server => Data Export => Table to Export => Seleciona o Banco de dados =>
Seleciona o Schemas Objects => Drump Structure and Dat => Export to Drump Project Folder(Pasta) ou Export to Self-Contained File(Arquivo)=>
Inclued Create Schema => Start Export */

# Criando Import do Banco de Dados:
/* Em Workbench
Clica em Server => Data Import => Import Options (Escolhe a opção que foi feita o Export) => 
Import from Drump Project Folder(Pasta) ou Import from Self-Contained File(Arquivo)=>
Localiza o arquivo salvo no not ou disco de armazenamento => Start Import.*/